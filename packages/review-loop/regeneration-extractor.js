function mergeTasks(existingTasks, newTasks) {
  const seen = new Set(existingTasks.map((task) => task.id || `${task.source}:${task.stepId}:${task.feedback}:${task.target}`));
  const merged = [...existingTasks];
  for (const task of newTasks) {
    const key = task.id || `${task.source}:${task.stepId}:${task.feedback}:${task.target}`;
    if (!seen.has(key)) {
      merged.push(task);
      seen.add(key);
    }
  }
  return merged;
}

function candidatesMarkdown({ workflowRelativePath, tasks }) {
  return `# Regeneration Candidates

Workflow run: ${workflowRelativePath}

| Priority | Step | Action | Target | Status |
| --- | --- | --- | --- | --- |
${tasks.map((task) => `| ${task.priority} | ${task.stepId} | ${task.action} | ${task.target} | ${task.status} |`).join("\n")}

## Tasks
${tasks.map((task) => `### ${task.id}
- Agent: ${task.agentId}
- Artifact: ${task.artifact}
- Target: ${task.target}
- Status: ${task.status}
- Feedback: ${task.feedback}
`).join("\n")}
`;
}

function extractRegenerationTasks({ workspaceRoot, workflowRoot }) {
  const summary = readJson(path.join(workflowRoot, "summary.json"), null);
  const plan = readJson(path.join(workflowRoot, "plan.json"), null);
  if (!summary || !plan) {
    throw new Error("Workflow summary or plan is missing.");
  }

  const now = new Date().toISOString();
  const workflowRelativePath = path.relative(workspaceRoot, workflowRoot);
  const tasks = [];

  for (const step of plan.steps) {
    const metadata = readJson(path.join(workflowRoot, "steps", step.id, "metadata.json"), {});
    const outputPath = path.join(workflowRoot, "steps", step.id, "output.md");
    const outputMarkdown = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
    const extracted = tasksFromStepOutput({ now, workflowRelativePath, step, outputMarkdown });
    if (extracted.length > 0) {
      tasks.push(...extracted);
      continue;
    }
    if (metadata.backend === "prompt-packet" || metadata.status === "saved_prompt_packet") {
      tasks.push(fallbackTaskForPromptPacket({ now, workflowRelativePath, step, metadata }));
    }
  }

  writeJson(path.join(workflowRoot, "regeneration-candidates.json"), { tasks });
  writeText(path.join(workflowRoot, "regeneration-candidates.md"), candidatesMarkdown({ workflowRelativePath, tasks }));

  const queuePath = path.join(workspaceRoot, "memory/regeneration-queue.json");
  const queue = readJson(queuePath, { tasks: [] });
  writeJson(queuePath, { tasks: mergeTasks(queue.tasks || [], tasks) });

  return tasks;
}

